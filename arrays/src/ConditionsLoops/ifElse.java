package ConditionsLoops;

public class ifElse 
{
	public static void main(String[] args) 
	{
		Integer time = 10;
		if ( time <  10 ) 
		{
			System.out.println("Good Morning! ");
		} else if ( time < 20 )
		{
			System.out.println("Good day!");
		}
		else 
		{
			System.out.println("Good evening");
		}
	}
}
